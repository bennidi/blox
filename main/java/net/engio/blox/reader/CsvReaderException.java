package net.engio.blox.reader;


public class CsvReaderException extends Exception {

	public CsvReaderException() {
		super();
	}

	public CsvReaderException(String arg0, Throwable arg1) {
		super(arg0, arg1);
	}

	public CsvReaderException(String arg0) {
		super(arg0);
	}

	public CsvReaderException(Throwable arg0) {
		super(arg0);
	}
	
	

}
